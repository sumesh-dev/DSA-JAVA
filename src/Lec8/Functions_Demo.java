package Lec8;

public class Functions_Demo {
	public static void main(String[] args) {
		int a = 6;
		int b = 7;
		int c = a + b;
		System.out.println(c);
		addition();
	}

	public static void addition() {
		int a = 9;
		int b = 11;
		sub();
		int c = a + b;
		System.out.println(c);
	}

	public static void sub() {
		int a = 9;
		int b = 11;
		int c = a - b;
		System.out.println(c);
	}
}